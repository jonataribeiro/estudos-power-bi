n1=34
n2=14
def somar(): #definição de uma função
    r=n1+n2
    print("Soma de " + str(n1) + " e " + str(n2) + " = " +str(r))
    print("Jônata Ribeiro")

def subtrair(): #definição de uma função
    r=n1-n2
    print("Subtração de " + str(n1) + " e " + str(n2) + " = " +str(r))
    print("Jônata Ribeiro")

somar()
subtrair()